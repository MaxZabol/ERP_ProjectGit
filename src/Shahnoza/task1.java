package Shahnoza;

public class task1 {
    public static void main(String[] args) {

        int a = 10;
        int b = 15;

        System.out.println("a = " + a);
        System.out.println("b = " + b);


        a = a + b;
        // a=25
        //b=15

        b = a - b;

        //b=10
        //a=25

        a = a - b;
        //a= 15
        //b= 10

        System.out.println("==============after swap=================");
        System.out.println("a = " + a);
        System.out.println("b = " + b);



    }
}


